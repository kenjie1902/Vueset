<template>
  <div>

    <canvas id="acquisitions"></canvas>
  </div>
</template>

<script setup>
import Chart from 'chart.js/auto';
import { ref, onMounted } from 'vue';

const chartCanvas = ref(null);

onMounted(() => {
  const canvasElement = document.getElementById('acquisitions');
  
  if (canvasElement) {
    const data = [
      { year: 2010, count: 10 },
      { year: 2011, count: 20 },
      { year: 2012, count: 15 },
      { year: 2013, count: 25 },
      { year: 2014, count: 22 },
      { year: 2015, count: 30 },
      { year: 2016, count: 28 },
    ];
    

    new Chart(canvasElement, {
      type: 'bar',
      data: {
        labels: data.map(row => row.year),
        datasets: [
          {
            label: 'Acquisitions by year',
            data: data.map(row => row.count),

            backgroundColor: 'rgba(255, 99, 132, 0.2)',
            borderColor: 'rgba(255, 99, 132, 1)',
            borderWidth: 1,
          },
        ],
      },
      options: {
        scales: {
          y: {
            beginAtZero: true,
          },
        },
      },
    });
  }
});
</script>
